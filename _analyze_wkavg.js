const lines = rawData.split('\n');
let skipped = 0, naCount = 0, dupes = 0;
for (const line of lines) {
  const parts = line.split('\t');
  const sku = (parts[0] || '').trim();
  const valStr = (parts[1] || '').trim();
  if (!sku) continue;
  if (valStr === '#N/A' || valStr === '') { naCount++; continue; }
  const wkAvg = parseFloat(valStr);
  if (isNaN(wkAvg)) { skipped++; continue; }
  if (parsed[sku] !== undefined) { dupes++; }
  parsed[sku] = wkAvg;
}

const totalParsed = Object.keys(parsed).length;
const withSales = Object.values(parsed).filter(v => v > 0).length;
const zeros = Object.values(parsed).filter(v => v === 0).length;

console.log('=== ANALISE DOS DADOS COLADOS (Wk/Avg) ===');
console.log('Total linhas: ' + lines.length);
console.log('SKUs unicos parseados: ' + totalParsed);
console.log('Com vendas > 0: ' + withSales);
console.log('Zerados: ' + zeros);
console.log('#N/A ignorados: ' + naCount);
console.log('Duplicados (ultimo valor usado): ' + dupes);

// Now fetch DB and compare
function fetchPage(offset) {
  return new Promise((resolve, reject) => {
    const url = 'https://iaqnxamnjftwqdbsnfyl.supabase.co/rest/v1/branch_avg_monthly_sales?select=product,avg_mth_main&order=product&offset=' + offset + '&limit=1000';
    https.get(url, { headers: { 'apikey': KEY, 'Authorization': 'Bearer ' + KEY } }, (res) => {
      let body = '';
      res.on('data', c => body += c);
      res.on('end', () => resolve(JSON.parse(body)));
    }).on('error', reject);
  });
}

(async () => {
  let all = [];
  let offset = 0;
  while (true) {
    const rows = await fetchPage(offset);
    all = all.concat(rows);
    if (rows.length < 1000) break;
    offset += 1000;
  }
  
  const dbByProduct = {};
  for (const r of all) dbByProduct[r.product] = r.avg_mth_main || 0;
  
  console.log('\nDB total: ' + all.length + ' produtos');
  
  // Compare: convert Wk/Avg to Month (x4.33) and compare with DB avg_mth_main
  let matchCount = 0, higherCount = 0, lowerCount = 0, notInDb = 0;
  const bigDiffs = [];
  let totalWkMth = 0, totalDbMth = 0;
  let notInDbList = [];
  
  for (const [sku, wkAvg] of Object.entries(parsed)) {
    const mthFromWk = Math.round(wkAvg * 4.33 * 10) / 10;
    totalWkMth += mthFromWk;
    
    if (dbByProduct[sku] === undefined) {
      notInDb++;
      if (wkAvg > 0) notInDbList.push({ sku, wkAvg });
      continue;
    }
    
    const dbMth = dbByProduct[sku];
    totalDbMth += dbMth;
    const diff = mthFromWk - dbMth;
    const pct = dbMth > 0 ? Math.round(diff / dbMth * 100) : (mthFromWk > 0 ? 999 : 0);
    
    if (Math.abs(diff) < 2) { matchCount++; }
    else if (diff > 0) {
      higherCount++;
      if (Math.abs(diff) > 20) bigDiffs.push({ sku, wkAvg, mthFromWk, dbMth, diff: Math.round(diff), pct });
    } else {
      lowerCount++;
      if (Math.abs(diff) > 20) bigDiffs.push({ sku, wkAvg, mthFromWk, dbMth, diff: Math.round(diff), pct });
    }
  }
  
  console.log('\n=== COMPARACAO: Wk/Avg*4.33 vs DB avg_mth_main ===');
  console.log('Praticamente iguais (diff < 2/mth): ' + matchCount);
  console.log('Wk/Avg MAIOR que DB: ' + higherCount);
  console.log('Wk/Avg MENOR que DB: ' + lowerCount);
  console.log('Nao encontrados no DB: ' + notInDb);
  
  // Sort big diffs by abs diff
  bigDiffs.sort((a, b) => Math.abs(b.diff) - Math.abs(a.diff));
  
  console.log('\n=== TOP 30 MAIORES DIFERENCAS (|diff| > 20/mth) ===');
  console.log('SKU'.padEnd(30) + ' Wk/Avg  Wk*4.33  DB/Mth   Diff    %');
  console.log('-'.repeat(90));
  for (const d of bigDiffs.slice(0, 30)) {
    const arrow = d.diff > 0 ? '+' : '';
    console.log(
      d.sku.padEnd(30) + 
      String(d.wkAvg).padStart(7) + '  ' +
      String(d.mthFromWk).padStart(7) + '  ' +
      String(d.dbMth).padStart(7) + '  ' +
      (arrow + d.diff).padStart(7) + '  ' +
      (arrow + d.pct + '%').padStart(8)
    );
  }
  
  console.log('\nTotal com diff > 20/mth: ' + bigDiffs.length);
  
  // R3542-TRI specifically
  const r3542 = parsed['R3542-TRI'];
  if (r3542 !== undefined) {
    console.log('\n--- R3542-TRI ---');
    console.log('Wk/Avg colado: ' + r3542);
    console.log('Mth (x4.33): ' + Math.round(r3542 * 4.33 * 10) / 10);
    console.log('DB avg_mth_main: ' + (dbByProduct['R3542-TRI'] || 'N/A'));
    console.log('DB avg_wk (DB/4.33): ' + Math.round((dbByProduct['R3542-TRI'] || 0) / 4.33 * 10) / 10);
  }
  
  // Key products check
  console.log('\n=== TOP PRODUTOS (por volume Wk/Avg) ===');
  const sorted = Object.entries(parsed).filter(([,v]) => v > 0).sort((a, b) => b[1] - a[1]);
  console.log('SKU'.padEnd(30) + ' Wk/Avg  Wk*4.33  DB/Mth  DB/Wk  Match?');
  console.log('-'.repeat(90));
  for (const [sku, wk] of sorted.slice(0, 25)) {
    const mth = Math.round(wk * 4.33 * 10) / 10;
    const dbMth = dbByProduct[sku] || 0;
    const dbWk = Math.round(dbMth / 4.33 * 10) / 10;
    const diff = Math.abs(mth - dbMth);
    const status = dbMth === 0 ? 'NOT IN DB' : (diff < 5 ? 'OK' : (diff < 50 ? 'DIFF' : 'BIG DIFF'));
    console.log(
      sku.padEnd(30) + 
      String(wk).padStart(7) + '  ' +
      String(mth).padStart(7) + '  ' +
      String(dbMth).padStart(7) + '  ' +
      String(dbWk).padStart(6) + '  ' +
      status
    );
  }
  
  // Products not in DB with sales > 1
  if (notInDbList.length > 0) {
    notInDbList.sort((a, b) => b.wkAvg - a.wkAvg);
    console.log('\n=== PRODUTOS COM VENDAS NAO ENCONTRADOS NO DB (top 20) ===');
    for (const { sku, wkAvg } of notInDbList.slice(0, 20)) {
      console.log('  ' + sku.padEnd(40) + ' Wk/Avg: ' + wkAvg);
    }
  }
  
  // Summary: what's the source?
  console.log('\n=== RESUMO ===');
  console.log('Este dataset parece ser um TARGET de restock semanal (Wk/Avg)');
  console.log('Para comparar com DB (que tem avg_mth_main), multiplico Wk/Avg x 4.33');
  console.log('Total volume semanal colado: ' + Object.values(parsed).reduce((a, b) => a + b, 0).toFixed(0) + ' un/semana');
  console.log('Total volume mensal equivalente: ' + totalWkMth.toFixed(0) + ' un/mes');
  console.log('Total DB mensal (correspondentes): ' + totalDbMth.toFixed(0) + ' un/mes');
  if (totalDbMth > 0) {
    const ratio = totalWkMth / totalDbMth;
    console.log('Ratio colado/DB: ' + ratio.toFixed(2) + 'x (' + (ratio > 1 ? '+' : '') + Math.round((ratio - 1) * 100) + '%)');
  }
})();
